import Foundation
import UIKit

extension UIFont {
    class func appFont12() -> UIFont {
        return UIFont.systemFont(ofSize: 12)
    }
    
    class func appFont14() -> UIFont {
        return UIFont.systemFont(ofSize: 14)
    }
    
    class func appFont16() -> UIFont {
        return UIFont.systemFont(ofSize: 16)
    }
    
    class func appFont18() -> UIFont {
        return UIFont.systemFont(ofSize: 18)
    }
    
    class func appFont20() -> UIFont {
        return UIFont.systemFont(ofSize: 20)
    }
}
